Science and Technology. IITB admits annually around 900 candidates for the Undergraduate programmes(B.Tech./
B.Tech + M.Tech Dual Degree/ 5 year Integrated M.Sc.) through Joint Entrance Examination (JEE). Keeping in
view the ever growing challenges and new developments, lIT Bombay recently revised its Undergraduate
Programme (w.e.f. the Academic year 2007) to address the specific needs of the country.
                </p>
                <p>
                	Undergraduate programmes consist of courses in basic sciences, humanities and social sciences, engineering and
technology and other related topics. At various stages of the programme, students are initiated into research
methodologies, library reference work, use of engineering and scientific equipments/instruments, learning of
modern computational techniques, writing of technical and scientific reports and effective communication. These
components constitute the requirements for the B.Tech./ B.Tech. + M.Tech. Dual Degree/ M.Sc. degree (Major
degree). Apart from the minimum credit requirements for the award of the degree, opportunities exist for
supplementing the learning experience by crediting additional courses, in diverse areas. These additional credits
when they are in focused areas can earn the students credentials like Minor/Honors. It should however be noted
that these additional learning courses do not contribute in any way or are in no form a requirement for the Major
degree of the student.
                </p>
                <p>
                	The new curriculum has reduced and reorganized the components of the Major degree providing surplus time to
the students for enhancing their academic learning experience, though the initiative is left entirely to them. They
can use it to credit an assortment of courses/ projects anywhere in the Institute, to gain a wide exposure. These
additional academic accomplishments will find a separate mention in the transcript. They can also credit focused
activities which can qualify as a minor/honors. They may alternatively devote part or all of the additional time for
extra-curricular activities and gain hands-on managerial/aesthetic skills or sensitivity towards social issues.
                </p>
                <p>
                	Minor is an additional credential a student will earn if s/he does 30 credits worth of additional learning in a
discipline other than her/his major discipline. All academic units in the Institute offer minors in their disciplines,
and prescribe a structure necessary for earning a minor in that discipline. Honors is an additional credential a
student will earn if s/he opts for the extra 30 credits needed for this in her/ his own discipline. The concerned
department specifies the course requirements for earning the Honors. Honors is a mandatory component for the
Dual degree programme. The Minor/Honors degree is awarded along with the Undergraduate degree and not in the
interim period.
                </p>
                <p>
                	The reporting of these additional courses in the interim period till graduation will be clubbed together under the
heading of Additional learning Performance and a Total Performance index is reported in the interim transcript of
the students. However, on graduation a separate header for Minor and Honors will be created if the student has
completed the requirements for that curricular component. It is again stressed that. these are not graduation
requirements but are additional learning opportunities. The performance in the major degree is a common
performance index for all students as it was earlier. The additional learning component is an expression of the
multidimensional interests of a student and hence the additional learning performance index (and subsequently the
total performance index) need to be interpreted subjectively.
